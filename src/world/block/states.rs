    #[inline]
    pub fn from_state(state: u16) -> Self {
        let id = state >> 4;
        let meta = (state & 0x0f) as u8;
        match id {
            1 => match meta & 0x07 {
                1 | 2 => Block::StoneGranite,
                3 | 4 => Block::StoneDiorite,
                5 | 6 => Block::StoneAndesite,
                _ => Block::Stone,
            },
            2 => {
                if meta & 0x01 != 0 {
                    Block::GrassSnowy
                } else {
                    Block::Grass
                }
            }
            17 => {
                if meta & 0x03 == 0 {
                    Block::Log
                } else {
                    Block::Log2
                }
            }
            18 => {
                if meta & 0x03 == 0 {
                    Block::Leaves
                } else {
                    Block::Leaves2
                }
            }
            43 => Block::DoubleStoneSlab,
            44 => Block::StoneSlab,
            61 => Block::Furnace,
            62 => Block::LitFurnace,
            73 => Block::RedstoneOre,
            74 => Block::LitRedstoneOre,
            75 => Block::UnlitRedstoneTorch,
            76 => Block::RedstoneTorch,
            93 => Block::UnpoweredRepeater,
            94 => Block::PoweredRepeater,
            123 => Block::RedstoneLamp,
            124 => Block::LitRedstoneLamp,
            125 => Block::DoubleWoodSlab,
            126 => Block::WoodSlab,
            161 => Block::Leaves2,
            162 => Block::Log2,
            168 => match meta {
                1 => Block::Prismarine, // prismarine bricks (same block for now)
                2 => Block::Prismarine, // dark prismarine (same block for now)
                _ => Block::Prismarine,
            },
            _ => Block::from_id(id),
        }
    }
